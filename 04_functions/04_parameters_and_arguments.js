// Parameters and arguments in JavaScript functions allow us to send additional information to our function.

// Parameters are placeholders defined in the function declaration.
// Arguments are the actual values passed when we invoke the function.

// Example-1:

// 'number' is a parameter in the function definition
function play(number) {
  console.log("The number is ", number);
}

// 'cricket' is an argument passed during the function call
play("0603");

// example-2

// 'name' is a parameter
function student(name) {
    console.log("Student name is", name);
}

// 'Muskan' is an argument
student("Muskan");

// example-3

// 'city' is a parameter
function location(city) {
    console.log("City is", city);
}

// 'Ahmedabad' is an argument
location("Ahmedabad");