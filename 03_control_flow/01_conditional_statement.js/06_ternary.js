// ternary is a short-cut method of if else condition

// example-1

let number = 8;

let result = (number % 2 === 0) ? "Even" : "Odd";

console.log(result);

// example-2

let age = 20;

let message = (age >= 18) ? "Eligible to Vote" : "Not Eligible to Vote";

console.log(message);

// example-3

let num1 = 25;
let num2 = 40;

let greater = (num1 > num2) ? num1 : num2;

console.log("Greater number is:", greater);