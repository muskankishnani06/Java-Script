// loop is used to perform task repeatedly

// In JavaScript, the for loop is used for iterating over a block of code a certain number of times,
// or for iterating over the elements of an array.

// example-1 of for loop 

for (let i = 1; i <= 100; i++) {
    console.log(i);
}

// example-2

let students = ["Rahul", "Priya", "Amit", "Neha", "Rohan"];

for (let i = 0; i < students.length; i++) {
    console.log(students[i]);
}

// example-3

let number = 5;

for (let i = 1; i <= 10; i++) {
    console.log(number + " x " + i + " = " + (number * i));
}