// The while loop repeatedly executes a block of code as long as a specified condition is true.

// example-1

let i = 1;

while (i <= 1000) {
    console.log(i);
    i++;
}

// example-2

let countdown = 5;

while (countdown > 0) {
    console.log("Countdown:", countdown);
    countdown--;
}

console.log("Time's Up!");

// example-3

let k = 1;

while (k <= 5) {
    console.log("Square of", k, "=", k * k);
    k++;
}